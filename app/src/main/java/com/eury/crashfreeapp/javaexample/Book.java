package com.eury.crashfreeapp.javaexample;

public class Book {
    private int id;
    private String title;
    private Integer year;
    private Author author;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public Integer getYear() {
        return year;
    }

    public Author getAuthor() {
        return author;
    }
}


